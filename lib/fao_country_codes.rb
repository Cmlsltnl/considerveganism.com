  def fao_to_iso3166_code(country)
    raise "code for #{country} not found" unless FaoCountryCodes::FAO_TO_ALPHA2[country]
    return FaoCountryCodes::FAO_TO_ALPHA2[country]
  end

  def fao_to_iso3166_name(country)
    if country.length != 2
      country = fao_to_iso3166_code(country)
    end
    name = ALPHA2_TO_COMMON[country] || (ISO3166::Country[country] && ISO3166::Country[country].name)
    raise "name for #{country} not found" unless name
    return name
  end
